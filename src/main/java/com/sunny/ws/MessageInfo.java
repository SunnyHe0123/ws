package com.sunny.ws;

public class MessageInfo {
   public String getName() {
      return name;
   }

   public void setName(String name) {
      this.name = name;
   }

   private String name;

   public String getMessage() {
      return message;
   }

   public void setMessage(String message) {
      this.message = message;
   }

   private String message;
}
